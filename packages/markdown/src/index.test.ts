import path from 'node:path';
import { describe, expect, it } from 'vitest';
import { loadAtlasGraph } from '@agent-atlas/core';
import { generateMarkdownViews, renderEntityCard, renderRepositoryReadme } from './index.js';

describe('generateMarkdownViews', () => {
  it('generates compact public markdown views', async () => {
    const graph = await loadAtlasGraph(path.resolve('../../examples/personal-ops-sanitized'));
    const files = generateMarkdownViews(graph, { profile: 'public' });
    const filePaths = files.map((file) => file.path);

    expect(filePaths).toContain('atlas.md');
    expect(filePaths).toContain('verification.md');
    expect(filePaths).toContain('domains/calendar.md');
    expect(filePaths).toContain('workflows/plan-week.md');
    expect(filePaths).toContain('components/weekly-planner.md');
    expect(filePaths).not.toContain('documents/weekly-planning-system.md');
    const root = files.find((file) => file.path === 'atlas.md')?.content ?? '';
    expect(root).toContain('<!-- Generated by Agent Atlas. Do not edit directly. -->');
    expect(root).toContain('# Atlas overview');
    expect(root).toContain('## Start here');
  });

  it('includes private-profile files when requested', async () => {
    const graph = await loadAtlasGraph(path.resolve('../../examples/personal-ops-sanitized'), {
      profile: 'private',
    });
    const files = generateMarkdownViews(graph, { profile: 'private' });

    expect(files.map((file) => file.path)).toContain('documents/weekly-planning-system.md');
    expect(files.find((file) => file.path === 'documents/weekly-planning-system.md')?.content).toContain(
      'notion://page/sanitized-weekly-planning',
    );
  });

  it('redacts private references from public markdown cards', () => {
    const markdown = renderEntityCard(
      {
        id: 'document:release-process',
        kind: 'document',
        title: 'Release Process',
        summary: 'Internal release process.',
        visibility: 'public',
        uri: 'notion://page/sanitized-release-process',
      },
      [],
      'public',
    );

    expect(markdown).toContain('[redacted: private reference]');
    expect(markdown).not.toContain('notion://page/sanitized-release-process');
  });

  it('renders a compact repository README from visible atlas entities', () => {
    const markdown = renderRepositoryReadme(
      {
        rootPath: '.',
        index: {
          entitiesById: new Map(),
          outgoingById: new Map(),
          incomingById: new Map(),
        },
        diagnostics: [],
        entities: [
          {
            id: 'repository:example',
            kind: 'repository',
            title: 'example',
            summary: 'Example repository.',
            visibility: 'public',
            commands: [{ command: 'pnpm test', purpose: 'Run tests.' }],
            relations: [
              { type: 'contains', target: 'workflow:ship' },
              { type: 'contains', target: 'document:docs-index' },
            ],
          },
          {
            id: 'workflow:ship',
            kind: 'workflow',
            title: 'Ship',
            summary: 'Release the package.',
            visibility: 'public',
          },
          {
            id: 'document:docs-index',
            kind: 'document',
            title: 'Docs Index',
            summary: 'Documentation entrypoint.',
            visibility: 'public',
            uri: 'docs/index.md',
          },
          {
            id: 'document:private-runbook',
            kind: 'document',
            title: 'Private Runbook',
            summary: 'Private operational runbook.',
            visibility: 'private',
            uri: 'notion://page/private',
          },
        ],
        edges: [
          { source: 'repository:example', target: 'workflow:ship', type: 'contains', provenance: 'explicit' },
          { source: 'repository:example', target: 'document:docs-index', type: 'contains', provenance: 'explicit' },
        ],
      },
      { profile: 'public' },
    );

    expect(markdown).toContain('<!-- Generated by Agent Atlas. Do not edit directly. -->');
    expect(markdown).toContain('# example');
    expect(markdown).toContain('Documentation entrypoint: [Docs Index](docs/index.md).');
    expect(markdown).toContain('`workflow:ship`');
    expect(markdown).toContain('`pnpm test` - Run tests.');
    expect(markdown).not.toContain('Private Runbook');
    expect(markdown).not.toContain('notion://page/private');
  });
});
