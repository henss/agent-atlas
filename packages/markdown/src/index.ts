import type { AtlasEntity, AtlasEntityId, AtlasEntityKind } from '@agent-atlas/schema';
import {
  createAtlasOverview,
  createGraphIndex,
  renderAtlasOverviewMarkdown,
  type AtlasGraph,
  type AtlasGraphEdge,
} from '@agent-atlas/core';
import type { AtlasProfile } from '@agent-atlas/core';

export type MarkdownProfile = AtlasProfile;

export interface MarkdownGenerationOptions {
  profile?: MarkdownProfile;
}

export interface GeneratedMarkdownFile {
  path: string;
  content: string;
}

const GENERATED_HEADER = '<!-- Generated by Agent Atlas. Do not edit directly. -->';

const CARD_KIND_DIRECTORIES: Partial<Record<AtlasEntityKind, string>> = {
  domain: 'domains',
  workflow: 'workflows',
  component: 'components',
  resource: 'resources',
  document: 'documents',
};

export function generateMarkdownViews(
  graph: AtlasGraph,
  options: MarkdownGenerationOptions = {},
): GeneratedMarkdownFile[] {
  const profile = options.profile ?? 'public';
  const entities = filterEntitiesByProfile(graph.entities, profile);
  const entityIds = new Set(entities.map((entity) => entity.id));
  const edges = graph.edges.filter((edge) => entityIds.has(edge.source) && entityIds.has(edge.target));
  const files: GeneratedMarkdownFile[] = [
    {
      path: 'atlas.md',
      content: renderRootAtlasView(entities, edges, profile),
    },
    {
      path: 'verification.md',
      content: renderVerificationView(entities, edges, profile),
    },
  ];

  for (const entity of entities) {
    const directory = CARD_KIND_DIRECTORIES[entity.kind];
    if (!directory) {
      continue;
    }

    files.push({
      path: `${directory}/${entitySlug(entity.id)}.md`,
      content: renderEntityCard(entity, edges, profile),
    });
  }

  return files.sort((left, right) => left.path.localeCompare(right.path));
}

export function renderEntityCard(
  entity: AtlasEntity,
  edges: AtlasGraphEdge[] = [],
  profile: MarkdownProfile = 'public',
): string {
  const outgoing = edges.filter((edge) => edge.source === entity.id);
  const verification = outgoing.filter((edge) => edge.type === 'tested-by');
  const lines = [
    GENERATED_HEADER,
    '',
    `# ${entity.title}`,
    '',
    `ID: \`${entity.id}\``,
    `Kind: \`${entity.kind}\``,
    `Profile: \`${profile}\``,
    '',
    entity.summary,
  ];

  const metadata = renderMetadata(entity);
  if (metadata.length > 0) {
    lines.push('', '## Metadata', '', ...metadata);
  }

  const drillDown = renderLocalDrillDown(entity, edges);
  if (drillDown.length > 0) {
    lines.push('', '## Drill Down', '', ...drillDown);
  }

  if (entity.code?.paths?.length || entity.code?.entrypoints?.length) {
    lines.push('', '## Code', '');
    for (const codePath of entity.code.paths ?? []) {
      lines.push(`- path: \`${codePath}\``);
    }
    for (const entrypoint of entity.code.entrypoints ?? []) {
      lines.push(`- entrypoint: \`${entrypoint}\``);
    }
  }

  if (entity.uri || entity.access) {
    lines.push('', '## Access', '');
    if (entity.uri) {
      lines.push(`- uri: \`${renderUri(entity.uri, profile)}\``);
    }
    if (entity.access?.method) {
      lines.push(`- method: \`${entity.access.method}\``);
    }
    if (profile !== 'public' && entity.access?.server) {
      lines.push(`- server: \`${entity.access.server}\``);
    }
    if (profile !== 'public' && entity.access?.permission) {
      lines.push(`- permission: \`${entity.access.permission}\``);
    }
    if (entity.access?.private_overlay_required || isPrivateUri(entity.uri)) {
      lines.push('- private overlay required');
    }
  }

  lines.push('', '## Relations', '', renderRelationGroups(outgoing));

  if (verification.length > 0) {
    lines.push('', '## Verification', '');
    for (const edge of verification) {
      lines.push(`- \`${edge.target}\``);
    }
  }

  if (entity.agent) {
    lines.push('', '## Agent Hints', '');
    for (const hint of entity.agent.load_when ?? []) {
      lines.push(`- load when: ${hint}`);
    }
    for (const hint of entity.agent.avoid_loading_when ?? []) {
      lines.push(`- avoid when: ${hint}`);
    }
    if (entity.agent.token_budget_hint !== undefined) {
      lines.push(`- token budget hint: ${entity.agent.token_budget_hint}`);
    }
    for (const note of entity.agent.risk_notes ?? []) {
      lines.push(`- risk: ${note}`);
    }
  }

  return `${lines.join('\n')}\n`;
}

function renderRootAtlasView(
  entities: AtlasEntity[],
  edges: AtlasGraphEdge[],
  profile: MarkdownProfile,
): string {
  const graph: AtlasGraph = {
    rootPath: '.',
    entities,
    edges,
    index: createGraphIndex(entities, edges),
    diagnostics: [],
  };
  return `${GENERATED_HEADER}\n\n${renderAtlasOverviewMarkdown(createAtlasOverview(graph, profile))}`;
}

function renderVerificationView(
  entities: AtlasEntity[],
  edges: AtlasGraphEdge[],
  profile: MarkdownProfile,
): string {
  const tests = entities.filter((entity) => entity.kind === 'test-scope');
  const lines = [
    GENERATED_HEADER,
    '',
    '# Verification',
    '',
    `Profile: \`${profile}\``,
    '',
    '## Test Scopes',
    '',
    renderEntityList(tests),
  ];

  for (const test of tests) {
    lines.push('', `## ${test.title}`, '', `ID: \`${test.id}\``, '', test.summary);
    for (const command of test.commands ?? []) {
      lines.push('', `- command: \`${command.command}\``);
      if (command.purpose) {
        lines.push(`  purpose: ${command.purpose}`);
      }
    }

    const verifies = edges.filter((edge) => edge.source === test.id && edge.type === 'verifies');
    if (verifies.length > 0) {
      lines.push('', 'Verifies:');
      for (const edge of verifies) {
        lines.push(`- \`${edge.target}\``);
      }
    }
  }

  return `${lines.join('\n')}\n`;
}

function filterEntitiesByProfile(entities: AtlasEntity[], profile: MarkdownProfile): AtlasEntity[] {
  if (profile !== 'public') {
    return [...entities].sort(compareEntities);
  }

  return entities
    .filter((entity) => entity.visibility === undefined || entity.visibility === 'public')
    .sort(compareEntities);
}

function renderMetadata(entity: AtlasEntity): string[] {
  const metadata: string[] = [];
  if (entity.status) {
    metadata.push(`- status: \`${entity.status}\``);
  }
  if (entity.visibility) {
    metadata.push(`- visibility: \`${entity.visibility}\``);
  }
  if (entity.tags?.length) {
    metadata.push(`- tags: ${entity.tags.map((tag) => `\`${tag}\``).join(', ')}`);
  }
  if (entity.owners?.length) {
    metadata.push(`- owners: ${entity.owners.join(', ')}`);
  }
  return metadata;
}

function renderLocalDrillDown(entity: AtlasEntity, edges: AtlasGraphEdge[]): string[] {
  if (entity.kind !== 'domain' && entity.kind !== 'workflow') {
    return [];
  }

  const lines: string[] = [];
  const workflows = relatedEntities(entity.id, 'contains', 'workflow', edges);
  const components = relatedEntities(entity.id, ['contains', 'uses', 'implemented-by'], 'component', edges);
  const documents = relatedEntities(entity.id, 'documented-in', 'document', edges);
  const tests = relatedEntities(entity.id, 'tested-by', 'test-scope', edges);

  if (workflows.length > 0) {
    lines.push('### Workflows', '', ...workflows.map((id) => `- \`${id}\``), '');
  }
  if (components.length > 0) {
    lines.push('### Components', '', ...components.map((id) => `- \`${id}\``), '');
  }
  if (documents.length > 0) {
    lines.push('### Documents', '', ...documents.map((id) => `- \`${id}\``), '');
  }
  if (tests.length > 0) {
    lines.push('### Tests', '', ...tests.map((id) => `- \`${id}\``));
  }

  return lines.filter((line, index, all) => !(line === '' && all[index - 1] === ''));
}

function relatedEntities(
  sourceId: AtlasEntityId,
  relationType: string | string[],
  targetKind: AtlasEntityKind,
  edges: AtlasGraphEdge[],
): AtlasEntityId[] {
  const relationTypes = Array.isArray(relationType) ? relationType : [relationType];
  return [
    ...new Set(
      edges
        .filter(
          (edge) =>
            edge.source === sourceId &&
            relationTypes.includes(edge.type) &&
            edge.target.startsWith(`${targetKind}:`),
        )
        .map((edge) => edge.target),
    ),
  ].sort();
}

function renderUri(uri: string, profile: MarkdownProfile): string {
  if (profile === 'public' && isPrivateUri(uri)) {
    return '[redacted: private reference]';
  }

  return uri;
}

function isPrivateUri(uri: string | undefined): boolean {
  if (!uri) {
    return false;
  }

  const lowerUri = uri.toLowerCase();
  return (
    ['notion:', 'confluence:', 'jira:', 'gdrive:', 'google-drive:', 'gcal:', 'slack:', 'mailto:'].some(
      (scheme) => lowerUri.startsWith(scheme),
    ) ||
    lowerUri.includes('localhost') ||
    /^https?:\/\/(10\.|127\.|192\.168\.|172\.(1[6-9]|2\d|3[0-1])\.)/.test(lowerUri)
  );
}

function renderRelationGroups(edges: AtlasGraphEdge[]): string {
  if (edges.length === 0) {
    return '- No relations declared.';
  }

  const grouped = new Map<string, AtlasGraphEdge[]>();
  for (const edge of edges) {
    const group = grouped.get(edge.type) ?? [];
    group.push(edge);
    grouped.set(edge.type, group);
  }

  return [...grouped.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .flatMap(([type, group]) => [
      `### \`${type}\``,
      '',
      ...group.map((edge) => `- \`${edge.target}\`${edge.provenance === 'generated' ? ' _(generated)_' : ''}`),
      '',
    ])
    .join('\n')
    .trimEnd();
}

function renderEntityList(entities: AtlasEntity[]): string {
  if (entities.length === 0) {
    return '- None.';
  }

  return entities
    .sort(compareEntities)
    .map((entity) => `- \`${entity.id}\` - ${entity.title}: ${entity.summary}`)
    .join('\n');
}

function entitySlug(id: AtlasEntityId): string {
  return id.slice(id.indexOf(':') + 1).replaceAll('/', '__');
}

function compareEntities(left: AtlasEntity, right: AtlasEntity): number {
  const kindCompare = left.kind.localeCompare(right.kind);
  if (kindCompare !== 0) return kindCompare;
  return left.id.localeCompare(right.id);
}
