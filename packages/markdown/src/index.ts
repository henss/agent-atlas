function collectReadmeCliInterfaces(
  repositoryId: AtlasEntityId | undefined,
  entities: AtlasEntity[],
  edges: AtlasGraphEdge[],
): AtlasEntity[] {
  const cliInterfaces = entities.filter((entity) => entity.kind === 'interface' && isRecord(entity.metadata?.cli));
  if (!repositoryId || cliInterfaces.length === 0) {
    return cliInterfaces.sort(compareCliInterfaces);
  }

  const relatedIds = new Set(
    edges
      .filter((edge) => edge.source === repositoryId || edge.target === repositoryId)
      .flatMap((edge) => [edge.source, edge.target])
      .filter((id) => id !== repositoryId),
  );
  const directlyRelated = cliInterfaces.filter((entity) => relatedIds.has(entity.id));
  return (directlyRelated.length > 0 ? directlyRelated : cliInterfaces).sort(compareCliInterfaces);
}

function humanRepositorySummary(repository: AtlasEntity | undefined): string {
  const title = repository?.title ?? 'This repository';
  const rawSummary = repository?.summary?.trim();
  if (rawSummary && new RegExp(`^${escapeRegExp(title)}\\s+is\\b`, 'i').test(rawSummary)) {
    return sentenceFromSummary(rawSummary);
  }
  const summary = summaryFragment(rawSummary ?? 'the capabilities described below');
  return `${title} is ${summaryWithArticle(summary)}.`;
}

