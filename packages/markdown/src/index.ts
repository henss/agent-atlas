import type { AtlasEntity, AtlasEntityId, AtlasEntityKind } from '@agent-atlas/schema';
import {
  createAtlasOverview,
  createGraphIndex,
  renderAtlasOverviewMarkdown,
  type AtlasGraph,
  type AtlasGraphEdge,
} from '@agent-atlas/core';
import type { AtlasProfile } from '@agent-atlas/core';

export type MarkdownProfile = AtlasProfile;

export interface MarkdownGenerationOptions {
  profile?: MarkdownProfile;
}

export interface GeneratedMarkdownFile {
  path: string;
  content: string;
}

interface ReadmeLinkItem {
  label: string;
  path: string;
  note?: string;
}

interface ReadmePathItem {
  path: string;
  note?: string;
}

interface ReadmeSection {
  title: string;
  items: string[];
}

interface ReadmeCliMetadata {
  overview?: string[];
  reference?: ReadmeLinkItem;
}

interface CliInterfaceGroup {
  title: string;
  description?: string;
  commands: string[];
  items: CliInterfaceItem[];
}

interface CliInterfaceItem {
  label: string;
  command: string;
  summary: string;
  importance?: string;
  lifecycle: string[];
  audience: string[];
  tasks: string[];
}

interface RepositoryReadmeMetadata {
  start_here?: string[];
  overview?: string[];
  current_truth_order?: string[];
  key_docs?: ReadmeLinkItem[];
  durable_state?: ReadmePathItem[];
  operational_notes?: string[];
  sections?: ReadmeSection[];
  cli?: ReadmeCliMetadata;
}

const GENERATED_HEADER = '<!-- Generated by Agent Atlas. Do not edit directly. -->';
const README_CLI_GROUP_COMMAND_LIMIT = 1;

const CARD_KIND_DIRECTORIES: Partial<Record<AtlasEntityKind, string>> = {
  domain: 'domains',
  workflow: 'workflows',
  component: 'components',
  capability: 'capabilities',
  interface: 'interfaces',
  resource: 'resources',
  document: 'documents',
};

export function generateMarkdownViews(
  graph: AtlasGraph,
  options: MarkdownGenerationOptions = {},
): GeneratedMarkdownFile[] {
  const profile = options.profile ?? 'public';
  const entities = filterEntitiesByProfile(graph.entities, profile);
  const entityIds = new Set(entities.map((entity) => entity.id));
  const edges = graph.edges.filter((edge) => entityIds.has(edge.source) && entityIds.has(edge.target));
  const files: GeneratedMarkdownFile[] = [
    {
      path: 'atlas.md',
      content: renderRootAtlasView(entities, edges, profile),
    },
    {
      path: 'verification.md',
      content: renderVerificationView(entities, edges, profile),
    },
  ];

  for (const entity of entities) {
    const directory = CARD_KIND_DIRECTORIES[entity.kind];
    if (!directory) {
      continue;
    }

    files.push({
      path: `${directory}/${entitySlug(entity.id)}.md`,
      content: renderEntityCard(entity, edges, profile),
    });
  }

  for (const [kind, directory] of Object.entries(CARD_KIND_DIRECTORIES) as Array<[AtlasEntityKind, string]>) {
    const directoryEntities = entities.filter((entity) => entity.kind === kind);
    if (directoryEntities.length === 0) {
      continue;
    }
    files.push({
      path: `${directory}/index.md`,
      content: renderEntityDirectoryIndex(kind, directoryEntities, profile),
    });
  }

  return files.sort((left, right) => left.path.localeCompare(right.path));
}

export function renderRepositoryReadme(
  graph: AtlasGraph,
  options: MarkdownGenerationOptions = {},
): string {
  const profile = options.profile ?? 'public';
  const entities = filterEntitiesByProfile(graph.entities, profile);
  const entityIds = new Set(entities.map((entity) => entity.id));
  const edges = graph.edges.filter((edge) => entityIds.has(edge.source) && entityIds.has(edge.target));
  const repository = entities.find((entity) => entity.kind === 'repository');
  const title = repository?.title ?? 'Repository';
  const summary = repository?.summary ?? 'Atlas-generated repository orientation.';
  const domains = collectReadmeEntities(undefined, 'domain', entities, edges).slice(0, 6);
  const workflows = collectReadmeEntities(repository?.id, 'workflow', entities, edges).slice(0, 8);
  const components = collectReadmeEntities(repository?.id, 'component', entities, edges)
    .filter((entity) => !isPackageComponent(entity))
    .slice(0, 10);
  const allDocuments = collectReadmeEntities(repository?.id, 'document', entities, edges)
    .filter((entity) => !isAgentSkillDocument(entity));
  const globalDocuments = entities
    .filter((entity) => entity.kind === 'document')
    .filter((entity) => !isAgentSkillDocument(entity));
  const documents = allDocuments.slice(0, 8);
  const tests = collectReadmeEntities(repository?.id, 'test-scope', entities, edges).slice(0, 8);
  const commands = collectReadmeCommands(repository, tests);
  const cliInterfaces = collectReadmeCliInterfaces(repository?.id, entities, edges);
  const readmeMetadata = readRepositoryReadmeMetadata(repository);
  const docsIndex = allDocuments.find((document) => document.uri === 'docs/index.md');
  const cliReference = readmeMetadata.cli?.reference ?? findCliReference(allDocuments) ?? findCliReference(globalDocuments);
  const cliGroups = groupCliInterfaces(cliInterfaces);
  const defaultKeyDocs = collectDefaultKeyDocs([...allDocuments, ...globalDocuments], cliReference);
  const defaultDurableState = collectDefaultDurableState(entities);
  const sessionStart = findSessionStartCommand(commands, cliGroups);
  const sourceNotes = [
    ...(repository?.agent?.risk_notes ?? []),
    'This README is generated from Agent Atlas metadata. Update `.agent-atlas/**` or the referenced canonical docs, then regenerate it.',
  ];
  const lines = [
    GENERATED_HEADER,
    '',
    `# ${title}`,
    '',
    humanRepositorySummary(repository),
    '',
    '## Quick Orientation',
    '',
    ...renderReadmeList(
      readmeMetadata.overview ??
        readmeMetadata.start_here ??
        buildDefaultQuickOrientation(docsIndex, cliGroups, cliReference),
    ),
  ];

  appendGeneratedOverviewSection(lines, readmeMetadata, repository, workflows, components, cliGroups);
  appendNormalUseSection(lines, readmeMetadata, sessionStart, cliGroups, tests, commands);
  appendCommonEntryPointsSection(lines, cliGroups);
  appendConfiguredSections(lines, readmeMetadata.sections);
  appendReadmeTextSection(lines, 'Current Truth Order', readmeMetadata.current_truth_order);
  appendCliSection(lines, readmeMetadata.cli, cliReference, cliGroups);
  appendReadmePathSection(lines, 'Where Things Live', readmeMetadata.durable_state ?? defaultDurableState);
  appendReadmeLinkSection(lines, 'Key Docs', readmeMetadata.key_docs ?? defaultKeyDocs);
  appendReadmeSection(lines, 'Domains', domains);
  appendReadmeSection(lines, 'Key Implementation Surfaces', components);
  appendDrillDownSection(lines, documents);
  appendVerificationSection(lines, tests, commands);
  appendReadmeTextSection(lines, 'Operational Notes', readmeMetadata.operational_notes);

  if (commands.length > 0 && cliGroups.length === 0) {
    lines.push('', '## Commands', '');
    for (const command of commands) {
      lines.push(`- \`${command.command}\`${command.purpose ? ` - ${command.purpose}` : ''}`);
    }
  }

  lines.push('', '## Generated Source', '', ...sourceNotes.map((note) => `- ${note}`));
  return `${lines.join('\n')}\n`;
}

export function renderPackageReadmes(
  graph: AtlasGraph,
  options: MarkdownGenerationOptions = {},
): GeneratedMarkdownFile[] {
  const profile = options.profile ?? 'public';
  const entities = filterEntitiesByProfile(graph.entities, profile);
  const entityIds = new Set(entities.map((entity) => entity.id));
  const edges = graph.edges.filter((edge) => entityIds.has(edge.source) && entityIds.has(edge.target));
  return entities
    .filter(isPackageComponent)
    .map((entity) => {
      const metadata = readPackageMetadata(entity);
      return metadata.root && metadata.root !== '.'
        ? {
            path: `${metadata.root}/README.md`,
            content: renderPackageReadme(entity, entities, edges, profile),
          }
        : undefined;
    })
    .filter((file): file is GeneratedMarkdownFile => file !== undefined)
    .sort((left, right) => left.path.localeCompare(right.path));
}

export function renderEntityCard(
  entity: AtlasEntity,
  edges: AtlasGraphEdge[] = [],
  profile: MarkdownProfile = 'public',
): string {
  const outgoing = edges.filter((edge) => edge.source === entity.id);
  const verification = outgoing.filter((edge) => edge.type === 'tested-by');
  const lines = [
    GENERATED_HEADER,
    '',
    `# ${entity.title}`,
    '',
    `ID: \`${entity.id}\``,
    `Kind: \`${entity.kind}\``,
    `Profile: \`${profile}\``,
    '',
    entity.summary,
  ];

  const metadata = renderMetadata(entity);
  if (metadata.length > 0) {
    lines.push('', '## Metadata', '', ...metadata);
  }

  const drillDown = renderLocalDrillDown(entity, edges);
  if (drillDown.length > 0) {
    lines.push('', '## Drill Down', '', ...drillDown);
  }

  if (entity.code?.paths?.length || entity.code?.entrypoints?.length) {
    lines.push('', '## Code', '');
    for (const codePath of entity.code.paths ?? []) {
      lines.push(`- path: \`${codePath}\``);
    }
    for (const entrypoint of entity.code.entrypoints ?? []) {
      lines.push(`- entrypoint: \`${entrypoint}\``);
    }
  }

  if (entity.uri || entity.access) {
    lines.push('', '## Access', '');
    if (entity.uri) {
      lines.push(`- uri: \`${renderUri(entity.uri, profile)}\``);
    }
    if (entity.access?.method) {
      lines.push(`- method: \`${entity.access.method}\``);
    }
    if (profile !== 'public' && entity.access?.server) {
      lines.push(`- server: \`${entity.access.server}\``);
    }
    if (profile !== 'public' && entity.access?.permission) {
      lines.push(`- permission: \`${entity.access.permission}\``);
    }
    if (entity.access?.private_overlay_required || isPrivateUri(entity.uri)) {
      lines.push('- private overlay required');
    }
  }

  lines.push('', '## Relations', '', renderRelationGroups(outgoing));

  if (verification.length > 0) {
    lines.push('', '## Verification', '');
    for (const edge of verification) {
      lines.push(`- \`${edge.target}\``);
    }
  }

  if (entity.agent) {
    lines.push('', '## Agent Hints', '');
    for (const hint of entity.agent.load_when ?? []) {
      lines.push(`- load when: ${hint}`);
    }
    for (const hint of entity.agent.avoid_loading_when ?? []) {
      lines.push(`- avoid when: ${hint}`);
    }
    if (entity.agent.token_budget_hint !== undefined) {
      lines.push(`- token budget hint: ${entity.agent.token_budget_hint}`);
    }
    for (const note of entity.agent.risk_notes ?? []) {
      lines.push(`- risk: ${note}`);
    }
  }

  return `${lines.join('\n')}\n`;
}

function renderRootAtlasView(
  entities: AtlasEntity[],
  edges: AtlasGraphEdge[],
  profile: MarkdownProfile,
): string {
  const graph: AtlasGraph = {
    rootPath: '.',
    entities,
    edges,
    index: createGraphIndex(entities, edges),
    diagnostics: [],
  };
  return `${GENERATED_HEADER}\n\n${renderAtlasOverviewMarkdown(createAtlasOverview(graph, profile))}`;
}

function renderVerificationView(
  entities: AtlasEntity[],
  edges: AtlasGraphEdge[],
  profile: MarkdownProfile,
): string {
  const tests = entities.filter((entity) => entity.kind === 'test-scope');
  const lines = [
    GENERATED_HEADER,
    '',
    '# Verification',
    '',
    `Profile: \`${profile}\``,
    '',
    '## Test Scopes',
    '',
    renderEntityList(tests),
  ];

  for (const test of tests) {
    lines.push('', `## ${test.title}`, '', `ID: \`${test.id}\``, '', test.summary);
    for (const command of test.commands ?? []) {
      lines.push('', `- command: \`${command.command}\``);
      if (command.purpose) {
        lines.push(`  purpose: ${command.purpose}`);
      }
    }

    const verifies = edges.filter((edge) => edge.source === test.id && edge.type === 'verifies');
    if (verifies.length > 0) {
      lines.push('', 'Verifies:');
      for (const edge of verifies) {
        lines.push(`- \`${edge.target}\``);
      }
    }
  }

  return `${lines.join('\n')}\n`;
}

function renderEntityDirectoryIndex(
  kind: AtlasEntityKind,
  entities: AtlasEntity[],
  profile: MarkdownProfile,
): string {
  const title = titleCase(`${kind} index`);
  const lines = [
    GENERATED_HEADER,
    '',
    `# ${title}`,
    '',
    `Profile: \`${profile}\``,
    '',
    `Generated index of visible \`${kind}\` entities.`,
    '',
  ];

  const cliInterfaces = kind === 'interface' ? entities.filter((entity) => isRecord(entity.metadata?.cli)) : [];
  if (cliInterfaces.length > 0) {
    lines.push('## CLI Command Groups', '');
    for (const group of groupCliInterfaceLinks(cliInterfaces)) {
    lines.push(`- ${group.title} (${group.items.length})${group.description ? `: ${group.description} ` : ': '}${group.items.map((item) => `[${item.label}](${item.path})`).join(', ')}`);
    }
    const otherInterfaces = entities.filter((entity) => !isRecord(entity.metadata?.cli));
    if (otherInterfaces.length > 0) {
      lines.push('', '## Other Interfaces', '', ...renderLinkedEntityList(otherInterfaces));
    }
  } else {
    lines.push(...renderLinkedEntityList(entities));
  }

  return `${lines.join('\n')}\n`;
}

function filterEntitiesByProfile(entities: AtlasEntity[], profile: MarkdownProfile): AtlasEntity[] {
  if (profile !== 'public') {
    return [...entities].sort(compareEntities);
  }

  return entities
    .filter((entity) => entity.visibility === undefined || entity.visibility === 'public')
    .sort(compareEntities);
}

function renderMetadata(entity: AtlasEntity): string[] {
  const metadata: string[] = [];
  if (entity.status) {
    metadata.push(`- status: \`${entity.status}\``);
  }
  if (entity.visibility) {
    metadata.push(`- visibility: \`${entity.visibility}\``);
  }
  if (entity.tags?.length) {
    metadata.push(`- tags: ${entity.tags.map((tag) => `\`${tag}\``).join(', ')}`);
  }
  if (entity.owners?.length) {
    metadata.push(`- owners: ${entity.owners.join(', ')}`);
  }
  return metadata;
}

function renderLocalDrillDown(entity: AtlasEntity, edges: AtlasGraphEdge[]): string[] {
  if (isPackageComponent(entity)) {
    return renderPackageDrillDown(entity, edges);
  }

  if (entity.kind !== 'domain' && entity.kind !== 'workflow') {
    return [];
  }

  const lines: string[] = [];
  const workflows = relatedEntities(entity.id, 'contains', 'workflow', edges);
  const capabilities = relatedEntities(entity.id, ['contains', 'uses', 'implements'], 'capability', edges);
  const components = relatedEntities(entity.id, ['contains', 'uses', 'implemented-by'], 'component', edges);
  const documents = relatedEntities(entity.id, 'documented-in', 'document', edges);
  const tests = relatedEntities(entity.id, 'tested-by', 'test-scope', edges);

  if (workflows.length > 0) {
    lines.push('### Workflows', '', ...workflows.map((id) => `- \`${id}\``), '');
  }
  if (capabilities.length > 0) {
    lines.push('### Capabilities', '', ...capabilities.map((id) => `- \`${id}\``), '');
  }
  if (components.length > 0) {
    lines.push('### Components', '', ...components.map((id) => `- \`${id}\``), '');
  }
  if (documents.length > 0) {
    lines.push('### Documents', '', ...documents.map((id) => `- \`${id}\``), '');
  }
  if (tests.length > 0) {
    lines.push('### Tests', '', ...tests.map((id) => `- \`${id}\``));
  }

  return lines.filter((line, index, all) => !(line === '' && all[index - 1] === ''));
}

function renderPackageDrillDown(entity: AtlasEntity, edges: AtlasGraphEdge[]): string[] {
  const lines: string[] = [];
  const metadata = readPackageMetadata(entity);
  const packageFacts = [
    metadata.root ? `root: \`${metadata.root}\`` : undefined,
    metadata.path ? `manifest: \`${metadata.path}\`` : undefined,
    metadata.exports?.length ? `exports: ${metadata.exports.map((item) => `\`${item}\``).join(', ')}` : undefined,
  ].filter((item): item is string => Boolean(item));

  if (packageFacts.length > 0) {
    lines.push('### Package Facts', '', ...packageFacts.map((item) => `- ${item}`), '');
  }
  if (metadata.scripts?.length) {
    lines.push('### Scripts', '', ...metadata.scripts.map((script) => `- \`${script}\``), '');
  }
  appendPackageDrillDownGroup(lines, 'Interfaces', relatedEntities(entity.id, 'contains', 'interface', edges));
  appendPackageDrillDownGroup(lines, 'Components', relatedEntities(entity.id, 'contains', 'component', edges));
  appendPackageDrillDownGroup(lines, 'Documents', relatedEntities(entity.id, 'contains', 'document', edges));
  appendPackageDrillDownGroup(lines, 'Config', relatedEntities(entity.id, 'contains', 'resource', edges));
  appendPackageDrillDownGroup(lines, 'Tests', relatedEntities(entity.id, 'contains', 'test-scope', edges));
  return lines.filter((line, index, all) => !(line === '' && all[index - 1] === ''));
}

function renderPackageReadme(
  entity: AtlasEntity,
  entities: AtlasEntity[],
  edges: AtlasGraphEdge[],
  profile: MarkdownProfile,
): string {
  const metadata = readPackageMetadata(entity);
  const related = collectPackageReadmeRelated(entity, entities, edges);
  const commands = prioritizePackageCommands(entity.commands ?? []);
  const lines = [
    GENERATED_HEADER,
    '',
    `# ${entity.title}`,
    '',
    entity.summary,
    '',
    '## Quick Orientation',
    '',
    `- Package root: \`${metadata.root ?? '.'}\`.`,
    `- Manifest: \`${metadata.path ?? entity.uri ?? 'package.json'}\`.`,
    '- Use this README for package-local orientation; use the repo README and `docs/agents/atlas.md` for cross-package context.',
  ];

  const facts = [
    metadata.exports?.length ? `exports: ${metadata.exports.map((item) => `\`${item}\``).join(', ')}` : undefined,
    metadata.scripts?.length ? `scripts: ${metadata.scripts.map((item) => `\`${item}\``).join(', ')}` : undefined,
  ].filter((item): item is string => Boolean(item));
  if (facts.length > 0) {
    lines.push('', '## Package Facts', '', ...facts.map((fact) => `- ${fact}`));
  }

  const codeFacts = [
    ...(entity.code?.paths ?? []).map((item) => `path: \`${item}\``),
    ...(entity.code?.entrypoints ?? []).map((item) => `entrypoint: \`${item}\``),
  ];
  if (codeFacts.length > 0) {
    lines.push('', '## Code', '', ...codeFacts.map((fact) => `- ${fact}`));
  }

  if (commands.length > 0) {
    lines.push('', '## Normal Use', '');
    for (const command of commands.slice(0, 8)) {
      lines.push(`- \`${command.command}\`${command.purpose ? ` - ${command.purpose}` : ''}`);
    }
  }

  if (related.interfaces.length > 0) {
    lines.push('', '## Interfaces', '', ...related.interfaces.slice(0, 10).map(renderPackageInterfaceReadmeItem));
  }
  if (related.tests.length > 0) {
    lines.push('', '## Verification', '', ...related.tests.slice(0, 6).map((item) => `- \`${item.id}\` - ${item.summary}`));
  }
  if (related.documents.length > 0) {
    lines.push('', '## Docs', '');
    for (const document of related.documents.slice(0, 8)) {
      const target = document.uri && !isPrivateUri(document.uri) ? ` ([source](${renderUri(document.uri, profile)}))` : '';
      lines.push(`- \`${document.id}\` - ${document.title}: ${document.summary}${target}`);
    }
  }

  lines.push('', '## Generated Source', '', '- This package README is generated from Agent Atlas metadata and source-derived package surfaces.');
  return `${lines.join('\n')}\n`;
}

function renderPackageInterfaceReadmeItem(entity: AtlasEntity): string {
  const cli = isRecord(entity.metadata?.cli) ? entity.metadata.cli : undefined;
  const usage = readString(cli?.usage);
  const cliName = readString(cli?.cli_name);
  const command = readString(cli?.command);
  const executable = usage ?? [cliName, command].filter(Boolean).join(' ');
  if (executable) {
    return `- \`${executable}\` - ${entity.summary} (\`${entity.id}\`)`;
  }
  return `- \`${entity.id}\` - ${entity.summary}`;
}

function collectPackageReadmeRelated(
  entity: AtlasEntity,
  entities: AtlasEntity[],
  edges: AtlasGraphEdge[],
): { interfaces: AtlasEntity[]; tests: AtlasEntity[]; documents: AtlasEntity[] } {
  const relatedIds = new Set(
    edges
      .filter((edge) => edge.source === entity.id || edge.target === entity.id)
      .flatMap((edge) => [edge.source, edge.target])
      .filter((id) => id !== entity.id),
  );
  const related = entities.filter((candidate) => relatedIds.has(candidate.id)).sort(compareEntities);
  return {
    interfaces: related.filter((candidate) => candidate.kind === 'interface'),
    tests: related.filter((candidate) => candidate.kind === 'test-scope'),
    documents: related.filter((candidate) => candidate.kind === 'document' && !isAgentSkillDocument(candidate)),
  };
}

function prioritizePackageCommands(commands: NonNullable<AtlasEntity['commands']>): NonNullable<AtlasEntity['commands']> {
  const preferred = ['test', 'build', 'typecheck', 'lint', 'check', 'verify', 'start', 'dev'];
  return [...commands].sort((left, right) => {
    const leftName = left.command.split(/\s+/).at(-1) ?? left.command;
    const rightName = right.command.split(/\s+/).at(-1) ?? right.command;
    return preferredIndex(preferred, leftName) - preferredIndex(preferred, rightName) || left.command.localeCompare(right.command);
  });
}

function appendPackageDrillDownGroup(lines: string[], title: string, ids: AtlasEntityId[]): void {
  if (ids.length === 0) {
    return;
  }
  lines.push(`### ${title}`, '', ...ids.map((id) => `- \`${id}\``), '');
}

function relatedEntities(
  sourceId: AtlasEntityId,
  relationType: string | string[],
  targetKind: AtlasEntityKind,
  edges: AtlasGraphEdge[],
): AtlasEntityId[] {
  const relationTypes = Array.isArray(relationType) ? relationType : [relationType];
  return [
    ...new Set(
      edges
        .filter(
          (edge) =>
            edge.source === sourceId &&
            relationTypes.includes(edge.type) &&
            edge.target.startsWith(`${targetKind}:`),
        )
        .map((edge) => edge.target),
    ),
  ].sort();
}

function renderUri(uri: string, profile: MarkdownProfile): string {
  if (profile === 'public' && isPrivateUri(uri)) {
    return '[redacted: private reference]';
  }

  return uri;
}

function collectReadmeEntities(
  repositoryId: AtlasEntityId | undefined,
  kind: AtlasEntityKind,
  entities: AtlasEntity[],
  edges: AtlasGraphEdge[],
): AtlasEntity[] {
  const candidates = entities.filter((entity) => entity.kind === kind);
  if (!repositoryId) {
    return candidates.sort(compareEntities);
  }

  const relatedIds = new Set(
    edges
      .filter((edge) => edge.source === repositoryId || edge.target === repositoryId)
      .flatMap((edge) => [edge.source, edge.target])
      .filter((id) => id !== repositoryId),
  );
  const directlyRelated = candidates.filter((entity) => relatedIds.has(entity.id));
  return (directlyRelated.length > 0 ? directlyRelated : candidates).sort(compareEntities);
}

function appendReadmeSection(lines: string[], title: string, entities: AtlasEntity[]): void {
  if (entities.length === 0) {
    return;
  }

  lines.push('', `## ${title}`, '');
  for (const entity of entities) {
    const target = entity.uri && !isPrivateUri(entity.uri) ? ` ([source](${entity.uri}))` : '';
    lines.push(`- \`${entity.id}\` - ${entity.title}: ${entity.summary}${target}`);
  }
}

function appendDrillDownSection(lines: string[], documents: AtlasEntity[]): void {
  lines.push('', '## Drill Down', '');
  lines.push('- Atlas overview: `docs/agents/atlas.md`.');
  lines.push('- Task-scoped Atlas navigation: `atlas context-pack "<task>" --path . --budget 4000` or `atlas resolve-path <path> --path .`.');
  lines.push('- Entity indexes: `docs/agents/domains/`, `docs/agents/workflows/`, `docs/agents/components/`, and `docs/agents/capabilities/`.');
  lines.push('- Source-derived inventory: `docs/generated/source-derived-reference.md`.');
  const docsIndex = documents.find((document) => document.uri === 'docs/index.md');
  if (docsIndex?.uri) {
    lines.push(`- Documentation map: [${docsIndex.title}](${docsIndex.uri}).`);
  } else {
    lines.push('- Documentation map: `docs/index.md` when present.');
  }
}

function appendVerificationSection(
  lines: string[],
  tests: AtlasEntity[],
  repositoryCommands: NonNullable<AtlasEntity['commands']>,
): void {
  lines.push('', '## Verification', '');
  const verificationRepositoryCommands = repositoryCommands.filter((command) => /verify|test|check/i.test(command.command));
  const commands = prioritizeVerificationCommands([...verificationRepositoryCommands, ...collectTestCommands(tests)]).slice(0, 8);
  if (commands.length > 0) {
    for (const command of commands) {
      lines.push(`- \`${command.command}\`${command.purpose ? ` - ${command.purpose}` : ''}`);
    }
  } else {
    lines.push('- Use the repo-local test and check scripts advertised by package metadata.');
  }
  lines.push('- Scoped verification map: `docs/agents/verification.md`.');
}

function collectTestCommands(tests: AtlasEntity[]): NonNullable<AtlasEntity['commands']> {
  const seen = new Set<string>();
  const commands: NonNullable<AtlasEntity['commands']> = [];
  for (const command of tests.flatMap((test) => test.commands ?? [])) {
    if (seen.has(command.command)) {
      continue;
    }
    seen.add(command.command);
    commands.push(command);
  }
  return commands;
}

function prioritizeVerificationCommands(
  commands: NonNullable<AtlasEntity['commands']>,
): NonNullable<AtlasEntity['commands']> {
  const seen = new Set<string>();
  return [...commands]
    .sort((left, right) => verificationCommandRank(left.command) - verificationCommandRank(right.command))
    .filter((command) => {
      if (seen.has(command.command)) return false;
      seen.add(command.command);
      return true;
    });
}

function verificationCommandRank(command: string): number {
  if (/\bverify:session\b/.test(command)) return 0;
  if (/verify/i.test(command)) return 1;
  if (/test/i.test(command)) return 2;
  if (/check/i.test(command)) return 3;
  return 4;
}

